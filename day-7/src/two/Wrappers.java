package two;

public class Wrappers {
	public static void main(String[] args) {
		int myInt = 7;
		
		String myString = "5";
		
		//casting
		double myDouble = (double) myInt;
		
		//autoboxing
		Integer myInteger = myInt; 
		
		// unboxing
		myInt = Integer.parseInt(myString);
		
		
		// Wrapper Classes
		/* 
		 * Byte
		 * Short
		 * Integer
		 * Float
		 * Long
		 * Boolean
		 * Character
		 * 
		 **/
	}
}
