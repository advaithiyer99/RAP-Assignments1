package encapsulationExamples;

public class EncapTest {
	
	private String name;
	private String id;
	private int age;
	
	public String getName() {
		return name;
	}
	public void setName(String newName) {
		name = newName;
	}
	public String getId() {
		return id;
	}
	public void setId(String newId) {
		id = newId;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int newAge) {
		age = newAge;
	}
	
}
